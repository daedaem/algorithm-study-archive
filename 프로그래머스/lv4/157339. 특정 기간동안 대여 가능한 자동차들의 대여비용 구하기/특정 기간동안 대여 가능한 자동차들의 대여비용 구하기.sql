SELECT 
    TB.CAR_ID, 
    TB.CAR_TYPE, 
    FEE
FROM (
    SELECT 
        CC.CAR_ID, 
        CC.CAR_TYPE, 
        CC.DAILY_FEE,
        CP.DURATION_TYPE, 
        CP.DISCOUNT_RATE, 
        30 * CC.DAILY_FEE * ((100 - CP.DISCOUNT_RATE) / 100) AS FEE
    FROM 
        CAR_RENTAL_COMPANY_CAR CC
    LEFT JOIN 
        CAR_RENTAL_COMPANY_DISCOUNT_PLAN CP ON CC.CAR_TYPE = CP.CAR_TYPE
    WHERE 
        CC.CAR_TYPE IN ('세단', 'SUV')
        AND NOT EXISTS (
            SELECT 1 
            FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY CH
            WHERE CH.CAR_ID = CC.CAR_ID 
              AND (CH.START_DATE <= DATE '2022-11-30' AND CH.END_DATE >= DATE '2022-11-01')
        )
        AND CP.DURATION_TYPE = '30일 이상'
) TB
WHERE 
    (TB.FEE BETWEEN 500000 AND 2000000)
ORDER BY 
    TB.FEE DESC, 
    TB.CAR_TYPE, 
    TB.CAR_ID DESC;